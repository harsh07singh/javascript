//1 for string value 
const month = "Mar"
switch (month) {
    case "Jan":
        console.log("Jan");
        break;
         case "Feb":
        console.log("Feb");
        break;
         case "Mar":
        console.log("March");
        break;
    default:
        console.log("No result");
        break;
}

//2 for number value
const num = 2
